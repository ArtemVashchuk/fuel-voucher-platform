async function main() {
    console.log("🚀 Starting Extracted Data Seed...");

    const lines = rawData.split('\n').filter(l => l.trim() !== '' && !l.includes('Найменування'));

    // 1. Cleanup stations table - keep only main 4 brands
    console.log("Sweep: Cleaning up main stations table...");
    const coreBrands = ['okko', 'wog', 'upg', 'klo'];
    await db.delete(schema.stations).where(sql`id NOT IN (${sql.join(coreBrands.map(id => sql.raw(`'${id}'`)), sql`, `)})`);

    const stationsData = lines.map(line => {
        const parts = line.split(/\t+/);
        if (parts.length < 2) return null;

        const name = parts[0].trim();
        const address = parts[1].split('UKR')[0].trim();
        const typeInfo = parts[parts.length - 1].trim();

        // Node ID
        const id = name.toLowerCase().replace(/\s+/g, '-');

        return {
            id,
            stationId: 'okko', // Primary brand for these extracted nodes
            name: `${name} (${typeInfo.replace('Тип АЗС ', '')})`,
            address,
            stationType: typeInfo,
            lat: "50.4501", // Default to Kyiv
            lng: "30.5234"
        };
    }).filter(s => s !== null);

    console.log(`📍 Prepared ${stationsData.length} station nodes.`);

    for (const s of stationsData) {
        if (!s) continue;
        await db.insert(schema.stationNodes).values(s).onConflictDoUpdate({
            target: schema.stationNodes.id,
            set: {
                address: s.address,
                stationType: s.stationType,
                name: s.name,
                stationId: s.stationId
            }
        });
    }

    console.log("✅ Seeding (Nodes) Complete!");
    process.exit(0);
}

main().catch(err => {
    console.error("❌ Seeding Failed:", err);
    process.exit(1);
});
